/*----------------------------------------------------------------------
 * large_char	Display a large character starting at position (x,y)
 *----------------------------------------------------------------------*/

void large_char(int x, int y, char c)
{
    int cc, xx, yy;
    
    if ( ! inside_curses )
    {
	error("Should only be called from inside curses\n");
    }
    if (( c < 32) || (c > 126))
    {
	return;		/* Just return if an out of range character */
    }
    cc = (int) c - 32;

    for (xx = 0; xx < 6; xx++)
    {
	for (yy = 0; yy < 8; yy++)
	{
	    if (LargeFont[cc][yy][xx] == 1)
	    {
		attrset(A_BOLD | COLOR_PAIR(BLUE_ON_BLACK));
		mvaddch(y + yy, x + xx, ' ');
		attrset(A_BOLD | COLOR_PAIR(BLUE_ON_WHITE));
	    }
	    else
	    {
		mvaddch(y + yy, x + xx, ' ');
	    }
	}
    }
    refresh();
}

